package net.ty.createcraftedbeginning.registry;

import net.createmod.catnip.gui.TextureSheetSegment;
import net.createmod.catnip.gui.UIRenderHelper;
import net.createmod.catnip.gui.element.ScreenElement;
import net.createmod.catnip.theme.Color;
import net.minecraft.client.gui.GuiGraphics;
import net.minecraft.resources.ResourceLocation;
import net.neoforged.api.distmarker.Dist;
import net.neoforged.api.distmarker.OnlyIn;
import net.ty.createcraftedbeginning.CreateCraftedBeginning;
import org.jetbrains.annotations.NotNull;

public enum CCBGUITextures implements ScreenElement, TextureSheetSegment {
    JEI_SHADOW("jei/widgets", 0, 56, 52, 11),
	JEI_DOWN_ARROW("jei/widgets", 0, 21, 18, 14),
    JEI_LONG_ARROW("jei/widgets", 19, 0, 71, 10),
    JEI_QUESTION_MARK("jei/widgets", 0, 178, 12, 16),
    JEI_COOLING("jei/widgets", 12, 178, 16, 16),
    JEI_COOLING_BACKGROUND("jei/widgets", 28, 178, 16, 16);

    public final ResourceLocation location;
	private final int width;
	private final int height;
	private final int startX;
	private final int startY;

    CCBGUITextures(String location, int width, int height) {
		this(location, 0, 0, width, height);
	}

	CCBGUITextures(String location, int startX, int startY, int width, int height) {
		this(CreateCraftedBeginning.MOD_ID, location, startX, startY, width, height);
	}

	CCBGUITextures(String namespace, String location, int startX, int startY, int width, int height) {
		this.location = ResourceLocation.fromNamespaceAndPath(namespace, "textures/gui/" + location + ".png");
		this.width = width;
		this.height = height;
		this.startX = startX;
		this.startY = startY;
	}

    @Override
	public @NotNull ResourceLocation getLocation() {
		return location;
	}

	@OnlyIn(Dist.CLIENT)
	public void render(GuiGraphics graphics, int x, int y) {
		graphics.blit(location, x, y, startX, startY, width, height);
	}

	@OnlyIn(Dist.CLIENT)
	public void render(GuiGraphics graphics, int x, int y, Color c) {
		bind();
		UIRenderHelper.drawColoredTexture(graphics, c, x, y, startX, startY, width, height);
	}

	@Override
	public int getStartX() {
		return startX;
	}

	@Override
	public int getStartY() {
		return startY;
	}

	@Override
	public int getWidth() {
		return width;
	}

	@Override
	public int getHeight() {
		return height;
	}
}
